package P1923.CEITI.GherdelescuGrigore;

public class Pilot {
    private String nume;
    private String prenume;

    public Pilot(String nume, String prenume) {
        this.nume = nume;
        this.prenume = prenume;
    }

    public String getNume() {
        return nume;
    }

    public String getPrenume() {
        return prenume;
    }
}
